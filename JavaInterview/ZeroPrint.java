public class ZeroPrint {
    public static void main(String[] args) {
        int i = 0;
        System.out.println("i++ ：" + i++);
        System.out.println("i-- ：" + i--);
        System.out.println("i+'0' ：" + i + '0');
        System.out.println("i: " + i);
    }

}
